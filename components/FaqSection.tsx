const FaqItem: React.FC<FaqItemProps> = ({ question, answer }) => {
  const [isOpen, setIsOpen] = useState(false);

  return (
    <div className="border-b border-gray-200 py-6">
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="flex justify-between items-center w-full text-left text-lg font-semibold text-primary-dark hover:text-primary transition-colors"
      >
        <span>{question}</span>
        {isOpen ? <ChevronUpIcon className="text-primary" /> : <ChevronDownIcon className="text-neutral-DEFAULT" />}
      </button>
      {isOpen && (
        <div className="mt-4 text-neutral-dark leading-relaxed pl-2 pr-6">
          {answer}
        </div>
      )}
    </div>
  );
};

